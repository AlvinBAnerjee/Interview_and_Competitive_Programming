package _12_Tree;

public class _2_size_of_binary_tree {
    static class  node{
        int data;
        node left;
        node right;
        node(int data)
        {
            this.data=data;
            this.left=null;
            this.right=null;
        }
    }

    public static void main(String[] args) {

                /*
                1
              /   \
             2     4
              \     \
               3     5
                    /
                   6

                 */

        node node1=new node(1);
        node node2=new node(2);
        node node3=new node(3);
        node node4=new node(4);
        node node5=new node(5);
        node node6=new node(6);
        node1.left=node2;
        node1.right=node4;
        node2.right=node3;
        node4.right=node5;
        node5.left=node6;
        System.out.println(get_size(node1));


    }
    static int get_size(node root)
    {
        if(root==null)
            return 0;
        else
            return 1+get_size(root.left)+get_size(root.right);
    }

}
